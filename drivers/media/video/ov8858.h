// R_gain, sensor red gain of AWB, 0x400 =1
// G_gain, sensor green gain of AWB, 0x400 =1
// B_gain, sensor blue gain of AWB, 0x400 =1
// return 0;
int ov8858_update_awb_gain(struct v4l2_subdev *sd, int R_gain, int G_gain, int B_gain)
{
	if (R_gain>0x400) {
		ov8858_write_otp(sd, 0x5032, R_gain>>8);
		ov8858_write_otp(sd, 0x5033, R_gain & 0x00ff);
	}
	if (G_gain>0x400) {
		ov8858_write_otp(sd, 0x5034, G_gain>>8);
		ov8858_write_otp(sd, 0x5035, G_gain & 0x00ff);
	}
	if (B_gain>0x400) {
		ov8858_write_otp(sd, 0x5036, B_gain>>8);
		ov8858_write_otp(sd, 0x5037, B_gain & 0x00ff);
	}
	return 0;
}

