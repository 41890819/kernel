// call this function after OV8858 initialization
// return value: 0 update success
// 1, no OTP
int ov8858_update_otp_wb(struct v4l2_subdev *sd)
{
	struct ov885x_otp_struct current_otp;
	int i;
	int otp_index;
	int temp;
	int rg,bg;
	int nR_G_gain, nB_G_gain, nG_G_gain;
	int R_gain, B_gain, G_gain;
	int nBase_gain;

	// R/G and B/G of current camera module is read out from sensor OTP
	// check first OTP with valid data
	for(i=1;i<=2;i++) {
		temp = ov8858_check_otp_wb(sd, i);
		if (temp == 2) {
			otp_index = i;
			break;
		}
	}
	if (i>2) {
		// no valid wb OTP data
		return 1;
	}
	ov8858_read_otp_wb(sd, otp_index, &current_otp);

	// no light source information in OTP, light factor = 1
	rg = current_otp.rg_ratio;
    
	// not light source information in OTP, light factor = 1
	bg = current_otp.bg_ratio;


	//calculate G gain
	nR_G_gain = (OV8858_RG_Ratio_Typical*1000) / rg;
	nB_G_gain = (OV8858_BG_Ratio_Typical*1000) / bg;
	nG_G_gain = 1000;
	if (nR_G_gain < 1000 || nB_G_gain < 1000)
		{
			if (nR_G_gain < nB_G_gain)
				nBase_gain = nR_G_gain;
			else
				nBase_gain = nB_G_gain;
		}
	else
		{
			nBase_gain = nG_G_gain;
		}
	R_gain = 0x400 * nR_G_gain / (nBase_gain);
	B_gain = 0x400 * nB_G_gain / (nBase_gain);
	G_gain = 0x400 * nG_G_gain / (nBase_gain);
	ov8858_update_awb_gain(sd, R_gain, G_gain, B_gain);
	return 0;
}

