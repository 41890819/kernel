struct regval_list {
	u8 reg_num;
	u8 value;
};

struct mode_list {
	u8 index;
	const struct regval_list *mode_regs;
};

/* Supported resolutions */
enum hi253_width {
	W_QCIF	= 176,
	W_CIF	= 352,
	W_VGA	= 640,
	W_SVGA	= 800,
	W_XGA	= 1024,
	W_SXGA	= 1280,
	W_UXGA	= 1600,
};

enum hi253_height {
	H_QCIF	= 144,
	H_CIF	= 288,
	H_VGA	= 480,
	H_SVGA	= 600,
	H_XGA	= 768,
	H_SXGA	= 1024,
	H_UXGA	= 1200,
};

struct hi253_win_size {
	char *name;
	enum hi253_width width;
	enum hi253_height height;
	const struct regval_list *regs;
};


struct hi253_priv {
	struct v4l2_subdev subdev;
	struct hi253_camera_info *info;
	enum v4l2_mbus_pixelcode cfmt_code;
	const struct hi253_win_size *win;
	int	model;
	u8 balance_value;
	u8 effect_value;
	u16	flag_vflip:1;
	u16	flag_hflip:1;
};

